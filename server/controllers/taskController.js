const leaveTask = asyncHandler(async (req, res) => {
  const { id } = req.params;
  const { userId } = req.user;

  try {
    const task = await Task.findById(id);
    if (!task) {
      return res.status(404).json({ status: false, message: "Task not found" });
    }

    const leavingUser = await User.findById(userId).select("name");

    task.team = task.team.filter((memberId) => memberId.toString() !== userId);

    const activity = {
      type: "left",
      activity: `${leavingUser.name} left the task.`,
      by: userId,
    };

    task.activities.push(activity);
    await task.save();

    const admins = await User.find({ isAdmin: true }).select("_id");

    await Notice.create({
      team: admins.map(admin => admin._id),
      text: `${leavingUser.name} left the task "${task.title}".`,
      task: task._id,
    });

    res.status(200).json({
      status: true,
      message: "You have left the task successfully.",
    });
  } catch (error) {
    console.log(error);
    res.status(500).json({ status: false, message: "Failed to leave task", error });
  }
});

